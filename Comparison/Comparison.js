"use strict";

console.log("Comparison Operators");

let intValue = 70;
let StringValue = "70";

console.log("intValue == stringValue is", intValue == StringValue);
console.log("intValue === stringValue is", intValue === StringValue);
console.log("intValue != stringValue is", intValue != StringValue);
console.log("intValue !== stringValue is", intValue !== StringValue);

console.log("intValue > stringValue is", intValue > StringValue);
console.log("intValue < stringValue is", intValue < StringValue);
console.log("intValue >= stringValue is", intValue >= StringValue);
console.log("intValue <= stringValue is", intValue <= StringValue);

